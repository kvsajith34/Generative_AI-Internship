// Helper to get destinations by continent
export const getDestinationsByContinent = (continent: string): Destination[] => {
  return destinations.filter((d) => d.continent === continent);
};

// Helper to get destinations by best-for tag
export const getDestinationsByTag = (tag: string): Destination[] => {
  return destinations.filter((d) =>
    d.bestFor.some((b) => b.toLowerCase().includes(tag.toLowerCase()))
  );
};

